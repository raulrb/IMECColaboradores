package BO;

public class ColaboradorBo {

	private String nombre;
	private String apellidos;
	private int cedula;
	private int telefonoCasa;
	private int telefonoCelular;
	private String correo;
	private String direccion;
	private int nivelIngles;
	private String observaciones;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidos() {
		return apellidos;
	}
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
	public int getCedula() {
		return cedula;
	}
	public void setCedula(int cedula) {
		this.cedula = cedula;
	}
	public int getTelefonoCasa() {
		return telefonoCasa;
	}
	public void setTelefonoCasa(int telefonoCasa) {
		this.telefonoCasa = telefonoCasa;
	}
	public int getTelefonoCelular() {
		return telefonoCelular;
	}
	public void setTelefonoCelular(int telefonoCelular) {
		this.telefonoCelular = telefonoCelular;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public int getNivelIngles() {
		return nivelIngles;
	}
	public void setNivelIngles(int nivelIngles) {
		this.nivelIngles = nivelIngles;
	}
	public String getObservaciones() {
		return observaciones;
	}
	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}
	
}
